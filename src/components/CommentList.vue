<template>
<div class="comment-list">
	<div class="page-title" v-if="comments.length">评论</div>
	<div class="comment" :key="index" v-for="comment in comments" @click="handleClick(comment)">
		<div class="user">
			<div class="inline">
				<img class="avatar" :src="comment.image" mode="aspectFit">
				{{ comment.title }}
			</div>
			<div class="right">
				{{ comment.location || '未知地点' }}
				<span class="text-primary">||</span>
				{{ comment.phone || '未知型号' }}
			</div>
		</div>
		<div class="content">{{ comment.comment }}</div>
	</div>
</div>
</template>

<script>
export default {
	props: ['comments', 'type'],
	methods: {
		handleClick(comment) {
			if (this.type === 'user') {
				wx.navigateTo({
					url: `/pages/detail/main?id=${comment.bookid}`
				});
			}
		}
	}
};
</script>

